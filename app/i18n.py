def normalize_language(value):
    code = str(value or "").strip()
    return code if code in LANGUAGE_CODES else DEFAULT_LANGUAGE


def language_label(code):
    normalized = normalize_language(code)
    for candidate, label in LANGUAGE_CHOICES:
        if candidate == normalized:
            return label
    return LANGUAGE_CHOICES[0][1]


def language_code(label):
    value = str(label or "").strip()
    for code, candidate in LANGUAGE_CHOICES:
        if value == candidate or value == code:
            return code
    return DEFAULT_LANGUAGE


def tr(language, key, **values):
    code = normalize_language(language)
    template = _TEXT.get(code, {}).get(key)
    if template is None:
        template = _TEXT[DEFAULT_LANGUAGE].get(key, key)
    return template.format(**values) if values else template
